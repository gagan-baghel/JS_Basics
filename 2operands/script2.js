let input = prompt("Enter the electric appliance you want to switch on");
switch (input) {
	    case "TV":
		alert("TV is switched on");
		console.log("TV is switched on");
		break;
	    case "AC":
		alert("AC is switched on");
		break;
	    case "Fridge":
		alert("Fridge is switched on");
		break;
	    case "Washing Machine":
		alert("Washing Machine is switched on");
		break;
	    case "Computer":
		alert("Computer is switched on");
		break;
	    default:
		alert("Invalid input");
}